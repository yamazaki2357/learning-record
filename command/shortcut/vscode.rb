vscodeのショートカット

# ・編集

# カーソルがある行の下に空行を挿入
# ⌘ + Enter

# カーソルがある行の上に空行を挿入
# ⌘ + Shift + Enter

# ・移動

# カーソル行の内容を↓に移動
# option + ↓

# カーソル行の内容を↑に移動
# option + ↑

# カーソル行の内容を↓にコピー
# option + shift + ↓

# カーソル行の内容を↑にコピー
# option + shift + ↑

# ・コメント

# 行コメントにする
# ⌘ + /

# 行コメントを外す
# ⌘ + /

# ブロックコメントにする
# option + shift + a

# ブロックコメントをはずす
# option + shift + a

・表示

折り返し位置切り替え
option + z

フォントサイズ等を大きくする
⌘ + shift + ;

フォントサイズ等を小さくする
⌘ + -

・選択

カーソルの単語選択
⌘ + d

カーソルの単語4つ選択
⌘ + d、⌘ + d、⌘ + d

現在行を選択
⌘ + l

現在行から下に2行選択
⌘ + l, ⌘ + l

アクションスマートセレクトを2つ広げる
⌘ + shift + contrl + →, ⌘ + shift + contrl + →

アクションスマートセレクトを2つ縮める
⌘ + shift + contrl + ←, ⌘ + shift + contrl + ←

・選択/置換

エディタ内の検索
⌘ + f

エディタ内の置換
⌘ + option + f

フォルダ/ワークスペース全体を検索
⌘ + shift + f

フォルダ/ワークスペース全体を置換
⌘ + shift + h

・UI操作

サイドバーを開く
⌘ + b

サイドバーを閉じる
⌘ + b

エクスプローラーを開く
⌘ + shift + e

検索を開く
⌘ + shift + f

設定画面を開く
⌘ + ,

ソース管理を開く
contrl + shift + g

デバッグを開く
⌘ + shift + d

拡張機能を開く
⌘ + shift + x

パネルを開く
⌘ + j

パネルを閉じる
⌘ + j

