出力を開く
⌘ + shift + u

出力を閉じる
⌘ + shift + u

問題を開く
⌘ + shift + m

問題を閉じる
⌘ + shift + m

デバッグコンソールを開く
⌘ + shift + y

デバッグコンソールを閉じる
⌘ + shift + y

・基本操作

上書き保存
⌘ + s

名前をつけて保存
⌘ + shift + s

・エディタ

2つ目のエディタグループを作成
⌘ + 2

1つ目のエディタグループに移動
⌘ + 1